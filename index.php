   <p> My name is Jim White and I grew up in Durham, North Carolina, but now live in Cary, North Carolina. I received my BA at UNC and majored in <a href="http://www.physics.unc.edu/">physics</a> and <a href="http://psychology.unc.edu/">psychology</a>. I also have a MS from NCSU from the <a href="http://www.mse.ncsu.edu/">materials engineering</a> department.During the 1990s I worked in telecommunications for Nortel Networks at Research Triangle Park. From 2006 to 2019 I worked at North Carolina State University part-time at the <a href="http://www.basic.ncsu.edu/">Biodiversity and Spatial Information Center</a> developing web mapping application, and now full-time as a programmer with the department of <a href="https://cals.ncsu.edu/entomology-and-plant-pathology/people/jbwhite2/">Entomology and Plant Pathology</a>. A page I made while I was working as a contractor is <a href="http://whitecodingandmaps.com">White Coding and Maps</a>.

