Wyllynghale Doo

[Latin] On the roll of view of frankpledge with court held Tuesday 10th September 24 Henry VIII [1532] among other things it is recorded that

At this court come Thomas Barnarde &amp; Margaret his wife in person (she being examined in secret) and surrender into the hands of the lord the reversion of one tenement with customary land belonging to the same, with appurtenances called Newmans, and of one tenement with 12 acres of land called Gowres to the use of Richard Barnard, son of Thomas &amp; Margaret.  To whom is granted seisin, to hold to him &amp; his assigns at the will of the lord, for rent &amp; services &amp;c under certain conditions specified in the indenture made between Thomas, Margaret &amp; Richard.  Richard should pay fines on the deaths of Thomas and Margaret &amp;c

###############################################

[Latin] To all the faithful in Christ &amp;c Richard Sampford of Shellow Bowelles in Essex Yoman, greetings.  Let it be known that I, Richard Sampford, according to a grant &amp; agreement dated 2nd January last past between me and a certain Richard Bernard of Roding Bernars in Essex yoman, have granted &amp; confirmed by this present indenture two messuages or tenements called Newmans &amp; Gowers with all gardens appertaining to the same, and also 7 crofts of land containing about 30 acres, and two meadows containing about 2 acres, now enclosed, in Wyllinghale Doo in Essex, now or lately in the occupation of the said Richard Bernard or his assigns, to have &amp; to hold to him, his heirs &amp; assigns forever, of the chief lord of that fee, for service &amp; annual rent of 4d, of my manor of Waltons Hall.  If the rent falls into arrears, then I or my heirs or the owner of the manor of Waltons Hall, may distrain for the arrears due from Richard Bernard or his heirs upon the property.  In witness whereof I have put my seal to each part of the indenture.   5th January 5 Elizabeth I [1563]  Signed Rychard Sampford   witnessed by William Lynese clarke, Rychard Wethear, William Pole, Thomas Fox, John Willes &amp; Thomas Sampford

