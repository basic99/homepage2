[Latin] And that Totherus White &amp; John Luke are common bakers in several bakeries of the lord without the lord's licence, taking &amp; carrying away bread &amp;c.  Fined 20d each.

[Latin] And that Tocherus White &amp; John Luke are common breakers of fences of the lord &amp; his tenants there, to the damage &amp;c.  They are fined 2d each.

