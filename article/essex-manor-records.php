[marginal heading] Willyngale Doo

At the court held there on Tuesday next after the feast of the Translation of St Thomas the Martyr in the thirteenth year of the reign of King Henry the Seventh [10 July 1498], it was enrolled thus:

To this court came John Smyth of Hoton [Hutton] and surrendered in the full court all his  customary tenement with lands belonging to the same tenement called Newmans into the hand of the lord, for him &amp; his heirs, to the use of Robert White &amp; Joan the daughter of the same Robert &amp; Joan lately his wife &amp; formerly the wife of Thomas Smyth,  to which same Robert White &amp; Joan his daughter the lord, by his steward,  has granted seisin out of his hands by the virge, to hold to him and the heirs &amp; assign of the aforesaid Joan, at the will of the lord, according to the customs of the manor, for all ancient services owing for it.  But under this following condition, that if the aforesaid Robert White should pay or make payment to the aforesaid John Smyth or his true attorneys, executors or assigns 23 shillings 4 pence in the following form, namely at the feast of MIchaelmas next coming after the date of this court 6s 8d, and at the next court held there 6s 8d, and at the next court to be held there in the future after that 10s, then the present surrender should remain in force.  And if default is made in any payment of the aforesaid payments in part or in full, against the form beforewritten, that then it will be lawful for the said John Smyth, his heirs &amp; assigns, by the licence of the lord &amp; the court to re-enter the whole of the aforesaid tenement &amp; premises, to possess &amp; hold it as in his former estate, notwithstanding in any way this surrender.  And he gave the lord for a fine for the whole estate, to hold as appears in the court roll, &amp; he made fealty for it to the lord &amp; he was admitted tenant, holding of the lord in the form aforesaid.

