Southampton Will bk 4, p.644

In the name of God Amen I Elizabeth Peirce of the Parish of Nottaway &amp;/ county of Southampton being low in health but of sound mind &amp; disposing meaning do make &amp;/ ordain this my last will &amp; testment in manner &amp; form following _ First I give to my/ daughter Elizabeth Peirce one bed &amp; furniture to be chosen by her from amongst all my/ beds to her &amp; her heirs forever _ Secondly I give to my daughters Martha Cobb and/ Elizabeth Peirce all my cupboard furniture to be equally divided between them _/ Thirdly It is my will &amp; desire that all my estate of what kind or nature soever/ to be sold &amp; the money arising from the sale after my just debts being paid to be/ divided as follows one fourth part to my son Peter Peirce one fourth part to my son/ Bolton Peirce &amp; the other half to be equally divided betwnn other children Spencer/ Peirce Nathaniel Peirce Martha Cobb &amp; Elizabeth to them &amp; their heirs forever Lastly/ I do nominate &amp; appoint Robert Goodwyn &amp; Joseph Vick Exors of this my last will &amp;/ testament &amp; I do revoke &amp; make void all other ... of wills by me made declare/ this to be my last will &amp; testament In witness whereof I have hereunto set my hand/ &amp; seal this 28th day of April 1794.
Signed Sealed Published
&amp; declared in presence of
Josiah Cashiele
Ja. Vick
Benj. Andrews
At a court held for the county of Southampton the 9th day of Oct 1794 this will/ was presented in court by Rich. Goodwyn one of the Exors therein named the other/ refusing proved by the oaths of Cashiel &amp; Jo Vick two of the witnesses thereto &amp;/ ordered to be recorded &amp; on the motion of the sd Exor oathe made according to lawe .../ is granted him for obtaining a probate thereof in due forme giving security whence from.
