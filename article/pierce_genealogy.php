  <p>This Pierce Welsh genealogy is based on my research and the use of <a href='https://research.aber.ac.uk/en/datasets/bartrum-genealogical-project' target='_blank'>Bartrum's Welsh pedigrees</a> to explore the earlier generations. The tribe of Gwenwys is descended from Brochwel Ysgythrog, king of Powys in the sixth century. The arms attibuted to Brochwel are the three nags pictured. Some detail on the ancestry of the Gwenwys tribe  is found in <i>Landed Gentry of Great Britain</i> (1900) in the entry for <a href='/img/Lloyd_Stockton_Manor.jpeg' target='_blank'>Lloyd of Stockton Manor</a>. Lloyd Piers, first cousin of Thomas of Virginia, adopted  these arms from his mother who was heiress of Griffith Lloyd. Here is a flag from a St. David's day parade that shows <a href='/img/St_Davids_Day_parade.jpg' target='_blank'>these arms</a> [<a target='_blank' href='https://commons.wikimedia.org/w/index.php?curid=44369182'>By&nbsp;Gareth&nbsp;James,&nbsp;CC&nbsp;BY&#8209;SA&nbsp;2.0</a>].</p>

  <p>Capt William Peirce and Thomas Pierce who both lived on Mulberry Island  in Virginia are generally considered to be related, although exactly how is not known. I have not been able to find that they were related through the Piers family, although they could have been. Most likely William Peirce's grandfather Piers Reynold of Welshpool, who living in 1535 had connections with Shrewsbury merchants, may have been related to the family of William Peers, Mercer of Shrewsbury, and who was grandfather of Thomas of Virginia. More details of my reasearch are in a manuscript <a href='/downloads/Capt_Wm_Peirce.pdf' target='_blank'>The Possible Welsh Ancestry of Captain William Peirce</a>.</p>

