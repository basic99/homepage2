To all xyian people to whome these presents shall come I John Whyte sends greeting &amp; know you that I the sd John Whyte for and in consideration of the some of twenty five hundred pounds of good tobacco &amp; cask for which I have recd. good caution for payment have bargained aliened sould enfeofed and confirmed &amp; by these presents doe bargain alien sell enfeofe and confirm unto Henry Culpepper a parcel of land abt. Two hundred twenty &amp; five acres more or less situate lying and being upon the north side of Little Creek joyning upon the land of Tho: Everidge wch said land was in the possesion of my father Tho: Whyte. To have and to hould the sd land wth all priveleges and immunitys whatsoever unto the sd. Henry Culpepper his heyres or assigns for ever &amp; I the said John Whyte do bind me my heyres executors or adminstrators in the full vallue there of that the sd Culpepper his heyres or assigns shall quietly occupie possess and enjoy the sd land without any lett trouble or hinderance from the the sd Whyte or any one by or from under me and doe hereby warrant the [?] of the sd land unto the sd Culpepper his heyres &amp;assigns for ever and shall for ever defend the same. In witness whereof I have herunto sett my hand &amp;seale this 15th day of April 1671
