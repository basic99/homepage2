<p>UNCOMMON GROUNDS
<p>DECEMBER 12, 2010
<p>Mark Pendergrast, Uncommon Grounds, The History of Coffee and How it Transformed our World, Revised Edition (New York: Basic Books, 2010)

<p>The book is concerned with the history of coffee and not much about drinking and fixing coffee. Mostly it discusses the production, sales and marketing after the American Civil War. Consequently there is a lot of detail about Brazilian economics and politics and the sales and marketing in the US. It is concerned mostly with the growth of large coffee companies in the first of the twentieth century, but does document the rise of specialty coffee in the last part of the book.

