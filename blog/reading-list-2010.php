<p>UNKNOWN QUANTITY
<p>NOVEMBER 30, 2010
<p>John Derbyshire, Unknown Quantity, A Real and Imaginary History of Algebra (New York: Plume, 2006)

<p>To quote the start of Chapter 11 – “Mathematics is, let’s face it, a dry subject, with little in the way of glamour or romance. The story of Evariste Galois has therefore been made much of by historians of Math.”

<p>He was killed at the age of 21 in a duel in France in 1830, and the night before he died he wrote a math paper that provided the foundations for Galois Theory.

<p>So that’s the most interesting part of the book and its difficult to see where he’s going if you are not a mathematician. However, it is an important story and he does a good job with the subject it seems to me – while giving account of the theoreticians who were discovering the foundations for advanced math.

