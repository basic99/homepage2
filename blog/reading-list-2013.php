<p>DARWIN’S GHOST: THE ORIGIN OF SPECIES UPDATED
<p>MARCH 31, 2014
<p>Steve Jones, Darwin’s Ghost: The Origin of Species Updated (New York: Random House, 2000). Book written by an academic geneticist in the form of Darwin’s The Origin of Species, but with the knowledge of biology of a modern scientist.

<hr>
<p>DID JESUS EXIST?: THE HISTORICAL ARGUMENT FOR JESUS OF NAZARETH
<p>MARCH 31, 2014
<p>Bart D. Ehrman, Did Jesus Exist?: The Historical Argument for Jesus of Nazareth (New York, Harper Collins, 2012) A book by an academic historian and agnostic/atheist presenting the case for existence of historical Jesus and some historical facts that can be surmised. Written in reply to books that make a case that Jesus was not a real historical person.

<hr>
<p>ZEALOT: THE LIFE AND TIMES OF JESUS OF NAZARETH
<p>MARCH 31, 2014
<p>Reza Aslan, Zealot: The Life and Times of Jesus of Nazareth (New York, Random House, 2013) Interesting and mostly well written book about historical Jesus. Parts of it were written more for dramatic effect than historical truth. Basic thesis that Jesus’ main aim was the overthrow of Roman rule and establishment of Jewish religious rule is not consistent with prevailing academic view that he was an apocalyptic prophet(per Ehrman).

<hr>
<p>RESTFUL WEB SERVICES
<p>SEPTEMBER 8, 2013
<p>Leonard Richardson and Sam Ruby, Restful Web Services (Sebastopol: O’Reilly, 2007) Technical book, have finished first half and skimmed rest/reference material. I recently took a week long course in using Python for web programming and this was the only book recommended aside from books about Python so I bought it. It is a method to provide web services that tries to work with  and use technologies implemented by the  http standards. The competing ways to do this are SOAP and XML/RPC which embeds function like calls and metadata in the request body.

<hr>
<p>WATCHING BASEBALL SMARTER
<p>SEPTEMBER 8, 2013
<p>Zack Hample, Watching Baseball Smarter (New York: Vintage Books, 2007) Good review of rules, strategy, pitching, etc. for baseball fans.

<hr>
<p>THE HOUSE OF MEDICI: ITS RISE AND FALL
<p>SEPTEMBER 8, 2013
<p>Christoper Hibbert, The House of Medici: Its Rise and Fall (New York: Morrow Quill, 1980) Renaissance, Florence, and Medici are all interrelated in this history of the Medici family. Story starts in 15th century and the rise of the Florentine  banking family, their becoming nobility and through to the last of the line in the 18th century.

<hr>
<p>FULL HOUSE
<p>SEPTEMBER 8, 2013
<p>Stephen Jay Gould, Full House (New York: Random House, 1996) Author weaves together his interest in baseball and the theory of evolution. Somehow manages to relate Ted Williams batting average to the importance of bacteria in understanding evolution.

<hr>
<p>THE STORY OF EARTH
<p>SEPTEMBER 8, 2013
<p>Robert M. Hazen, The Story of Earth (New York: Penguin, 2011) History of earth from formation ~4.6 billion years ago told by a geologist/paleontologist. Story is up to date with latest theories as is written by a professional in this field. Interesting story, recommended.

<hr>
<p>INTO THE HINTERLANDS
<p>SEPTEMBER 8, 2013
<p>David Drake and John Lambshead, Into the Hinterlands (Riverdale, NY: Baen, 2011). Science fiction story, outer space in the future. Flying through space-time on bicycles, fighting silicon based life forms, etc.

<hr>
<p>THE GOD DELUSION
<p>MARCH 4, 2013
<p>Richard Dawkins, The God Delusion (New York: Houghton Mifflin, 2006) A prominent biologist writes about his belief in the non-existence of God, especially the God of Jews, Christians and Muslims. This book was listed as one to be discussed by the Unitarian church theology book club, and I read it and went to those discussions.

<p>INTRODUCTION TO GRAPH THEORY
<p>MARCH 4, 2013
<p>Richard J. Trudeau, Introduction to Graph Theory(New York: Dover Publications, 1993) This is one of those nice math books that is made easier to follow without dumbing down. Not sure what practical uses the material has, but probably has some applications in computers and other areas. Basically, it discusses points and lines between them(edges and vertices) and topics such as whether you can draw the graph in the plane. A rather famous problem of this branch of math is the map coloring problem. The fact that a map can be covered with 5 colors is proved in the book, and the 4 color has been recently proved with the help of computers.

<hr>
<p>CHURCHILL: A STUDY IN GREATNESS
<p>DECEMBER 5, 2012
<p>Geoffrey Best, Churchill: A Study in Greatness(New York: Oxford University, 2003) Medium length (336 pages) biography of Churchill. Great man, good book.

<hr>
<p>THE ORIGINS OF THEORETICAL POPULATION GENETICS
<p>DECEMBER 5, 2012
<p>William B. Provine, The Origins of Theoretical Population Genetics(London: University of Chicago, 2001) As title suggests its a history of theoretical population genetics. In 1900 when the laws of Mendelian inheritance were re-discovered biologists were unable to reconcile them with the ideas of Darwinian natural selection. Most of the book (its not a long book – less than 200 pages) is concerned with the debates from 1900 to around 1930 during which time these 2 approaches were reconciled and modern population genetics emerged.

<hr>
<p>A HISTORY OF THE ARAB PEOPLES
<p>AUGUST 9, 2012
<p>Albert Hourani, A History of the Arab Peoples(New York: Warner Books, 1991) History starting with Arab peoples before the birth of Muhammed, his birth and history, Baghdad, Muslim society, the Ottoman empire and into the post WWII era. A little slow reading at times, but shows an important side of history to people raised in western culture.

<hr>
<p>JULIUS CAESAR
<p>AUGUST 9, 2012
<p>J.F.C. Fuller, Julius Caesar Man, Soldier, and Tyrant(New York: Da Capo, 1965) Story of Julius Caesar starting with history of Rome in the 100 years before his rise, and continuing through his Gallic Wars and the Civil War to his death. I happened to watch the movie Cleopatra while reading the book and it was interesting to compare them. The movie is actually pretty historic.

<hr>
<p>THE ENGLISH CIVIL WAR
<p>APRIL 28, 2012
<p>Diane Purkiss, The English Civil War(New York: Perseus Books, 2006). Interesting history of the historical background of the English Civil War and the war itself in the 1640s. Sometimes there was a lot of quoting of old letters and family details to show what it was really like for people, but also a lot of interesting historical detail I had not heard.  Not a great deal of military history, just several pages on each of  the major battles.

<hr>
<p>PROFESSIONAL WORDPRESS PLUGIN DEVELOPMENT
<p>APRIL 21, 2012
<p>Brad Williams, Ozh Richard, and Justin Tadlock, Professional WordPress Plugin Development (Indianapolis: Wiley, 2011). I read the first half of the book and parts of the second half that I needed to write my first WordPress plugin which I used to create map pages which can be seen under the computer section of this site.

<hr>
<p>CHAOS, MAKING A NEW SCIENCE
<p>MARCH 3, 2012
<p>James Gleick, Chaos Making a New Science (New York: Penguin Books, 1987). Interesting story about advances in understanding non-linear dynamics and applications to many different areas of science.

<hr>
<p>ON BASILISK STATION
<p>MARCH 3, 2012
<p>David Weber, On Basilisk Station (New York, 1993). Outer space science fiction.

<hr>
<p>THE QUANTUM STORY
<p>MARCH 3, 2012
<p>Jim Baggott, The Quantum Story (Oxford, 2011) A history of quantum mechanics from Planck to the present day.



