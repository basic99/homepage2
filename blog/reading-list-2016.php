<p>THE TROJAN WAR, A NEW HISTORY
<p>APRIL 16, 2017
<p>Barry Strauss, The Trojan War, A New History (New York: Simon & Schuster, 2006). An historical account of the Trojan War of the Iliad.

<hr>
<p>GENGHIS KHAN AND THE MAKING OF THE MODERN WORLD
<p>APRIL 16, 2017
<p>Jack Weatherford, Genghis Khan and the Making of the Modern World (New York: Broadway Books, 2004).  I visited a Genghis Khan exhibit at the Discovery Place museum in Charlotte, and bought this book in the bookstore on the way out.

<hr>
<p>THE ANCESTOR’S TALE, A PILGRIMAGE TO THE DAWN OF EVOLUTION
<p>JULY 25, 2016
<p>Richard Dawkins, The Ancestor’s Tale, A Pilgrimage to the Dawn of Evolution (New York: Mariner, 2004). A rather long book. It starts with humans and goes back in time to ancestors we shared with other things that are living today. A chapter for each common ancestor as you go back in time to common ancestor of fish, plants, fungi, bacteria, etc. At each chapter he uses it to illustrate some points about evolution.

<hr>
<p>MAGICAL MUSHROOMS, MISCHEVIOUS MOLDS
<p>JULY 25, 2016
<p>George Hudler, Magical Mushrooms, Mischevious Molds (Princeton, 1998). Book I read as an introduction to fungi because I work in a lab that does research on fungi.

<hr>
<p>THE SELFISH GENE
<p>APRIL 2, 2016
<p>Richard Dawkins, The Selfish Gene (Oxford, 1989). Probably Dawkins’ most popular and important work. Attempts to explain behaviour by what is most likely to transmit a gene.

<hr>
<p>THE AENEID
<p>APRIL 2, 2016
<p>Robert Fagles (translator) The Aeneid by Virgil (New York: Penguin, 2006). Classic work,  one of the most famous books ever written. Decided to read it after seeing it mentioned several times in the book by Mary Beard about Rome.

<hr>
<p>YOUR INNER FISH
<p>APRIL 2, 2016
<p>Neil Shubin, Your Inner Fish A Journey into the 3.5-billion-year History of the Human Body (New York: Vintage, 2008). The writer describes his discovery of the fossil record of the missing link between fish and land animals. He also describes the anatomical similarity of fishes and other animals in development of an embryo.

<hr>
<p>NEANDERTHAL MAN IN SEARCH OF LOST GENOMES
<p>APRIL 2, 2016
<p>Svante Paabo, Neanderthal Man In search of Lost Genomes (New York: Basic Books, 2014). Author writes about his lab’s research to extract dna from Neanderthal remains.

<hr>
<p>THE RED QUEEN: SEX AND THE EVOLUTION OF HUMAN NATURE
<p>APRIL 2, 2016
<p>Matt Ridley, The Red Queen: Sex and the Evolution of Human Nature (New York: Harper). The book starts at a genetic level giving theories for the evolution of sex and genetic recombination. Then it looks at sexual activity of animals and how it can be explained by its effect on transmission of genes. Lastly, he develops some theories explaining sexual preferences and habits of humans.

<hr>
<p>INFINITESIMAL: HOW A DANGEROUS MATHEMATICAL THEORY SHAPED THE MODERN WORLD
<p>APRIL 2, 2016
<p>Amir Alexander, Infinitesimal: How a Dangerous Mathematical Theory Shaped the Modern World (New York: MacMillan). This book is a history of the use of infinitesimal in math. It starts in Italy with conflict of Galileo and the Jesuit Catholics over the use of infinitesimals in math. The second part of the book covers the same debate in England between Thomas Hobbes and the English promoters of the use of infinitesimals.

<hr>
<p>SPQR A HISTORY OF ANCIENT ROME
<p>APRIL 2, 2016
<p>Mary Beard, SPQR A History of Ancient Rome (New York: Liveright Publishing) Kindle.  A history of Rome from its founding in pre-historic times up until the start of the 3rd century.

<hr>
<p>THE RISE OF THE TUDORS
<p>APRIL 25, 2015
<p>Chris Skidmore, The Rise of the Tudors, The Family that Changed English History (New York: St. Martins Press). Kindle edition. The story of the events leading to the Battle of Bosworth Field and the start of the Tudor kings and queens of England. Final chapter and postscript deal with recent archaeological searches for the site of the battle field and the discovery of the skeleton of Richard III.

<hr>
<p>THE BRUS FAMILY
<p>APRIL 25, 2015
<p>Ruth M. Blakely, The Brus Family in England and Scotland 1100-1295 (Woodbridge: The Boydell Press, 2005). My genealogy research has found that I might be related to this family(Humphrey White md. Elizabeth Bruce, 1586). The family in England is descended from Robert Brus who came from France around 1100 and was granted lands in the north of England. HIs sons Adam and Robert established the 2 branches of this family, the Lords of Annandale in Scotland from which the Scottish king is descended and the Lords of Skelton in Yorkshire. The book is well researched and seems to give an accurate history of the early years of this family. The Lords of Annandale were owners of the Manor of Writtle in the late 13th century, but it was taken away when Robert became king in 1306.

<hr>
<p>THE MANOR OF WRITTLE
<p>APRIL 25, 2015
<p>K.C. Newton, The Manor of Writtle (London: Phillimore, 1970). History of the Manor Writtle in Essex, mostly concerned with records of the Manor in the medieval times. Its related to my genealogy interests as it contains Roxwell the burial place of Humphrey White, and also the sub-manor of Benedict Otes which was owned by Thomas Varvell, a merchant with connections to Virginia.

<hr>
<p>BOCKING DEANERY
<p>APRIL 25, 2015
<p>Ann Hoffmann, Bocking Deanery, The Story of an Essex Peculiar(London: Phillimore, 1976). A history book I read related to my genealogy research interests in Essex.

<hr>
<p>THE DEMON-HAUNTED WORLD
<p>APRIL 25, 2015
<p>Carl Sagan, The Demon-Haunted World, Science as a Candle in the Dark (New York, 1996). Book read with Unitarian book club.

<hr>
<p>THE ENGLISH VILLAGE
<p>APRIL 25, 2015
<p>Richard Muir, The English Village (New York: Thames and Hudson, 1980). Picture book with text. History and description of English Village from earliest times.

<hr>
<p>MERCHANTS AND REVOLUTION
<p>MAY 23, 2014
<p>Robert Brenner, Merchants and Revolution – Commercial Change, Political Conflict, and London’s Overseas Traders, 1550-1653 (Princeton, 1993). The book describes the English merchants and their effects on politics starting with the Merchant Adventurers and their regulated cloth trade through to the new merchants trading largely in America and their influence on the civil war. It is a long and academic book (637 pages excluding the postscript which itself was another 80 pages which I skipped), but a good overview of its field and political forces at work in the civil war.

<hr>
<p>SCIENCE AND THE SEARCH FOR GOD
<p>MARCH 31, 2014
<p>Gary A. Kowalski, Science and the Search for God (New York: Lantern Books, 2003). Written by Unitarian minister attempting to present a theology that is not in conflict with modern scientific knowledge.

<hr>
<p>GARDEN OF MICROBIAL DELIGHTS
<p>MARCH 31, 2014
<p>Dorion Sagan and Lynn Margulis, Garden of Microbial Delights: A Practical Guide to the Subvisible World (Dubuque: Kendall Hunt, 1993). Picture book introduction to bacteria, protoctists, and fungi.

