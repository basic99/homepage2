<p>READING LIST
<p>THE BLACK BANNERS
<p>JANUARY 10, 2012
<p>Ali H. Soufan, The Black Banners, The Inside Story of 9/11 and the War Against al-Qaeda(New York: W.W. Norton, 2011). This  book, complete with redacted pages by the CIA, was  written by an Arabic speaking former FBI agent who interrogated many high level al-Qaeda captives. Before reading this book I didn’t really care too much about how al-Qaeda captives were interrogated. However, the writer makes a strong case from personal experience that he received more useful information without using enhanced interrogation techniques. Aside from that it gives a very good overview of the structure and growth of al-Qaeda in Sudan, Afghanistan, and Yemen, etc.

<hr>

<p>MAKERS OF MATHEMATICS
<p>SEPTEMBER 24, 2011
<p>Stuart Hollingdale, Makers of Mathematics (New York: Penguin Books, 1989) As title states a history of math. Equations are included, but are mostly just to illustrate points and generally I was not able to follow the math in his examples.
<hr>

<p>THE TEMPLARS
<p>SEPTEMBER 24, 2011
<p>Piers Paul Read, The Templars (New York: St. Martins, 1999) History of the Templars starting with background of the history of Jerusalem, and then the founding of the Templar order in 1119 in Jerusalem and their dissolution and trial at the hands of Phillip the Fair in 1307.

<hr>
<p>HTML 5 DESIGNING RICH INTERNET APPLICATIONS
<p>JUNE 30, 2011
<p>Matthew David, HTML 5 Designing Rich Internet Applications(Burlington, MA: Focal Press, 2010) Topic is obvious from the title. Good overview of using HTML 5 and its advanced features, some of which I probably won’t get around to using for a long time. Didn’t actually read all the material, but read enough to get me started with using HTML 5.

<hr>
<p>THE ELEMENTS OF TYPOGRAPHIC STYLE
<p>JUNE 30, 2011
<p>Robert Bringhurst, The Elements of Typographic Style (Vancouver: Hartley & Marks, 1992) Discussion of typography and page layout for books.

<hr>
<p>A HISTORY OF PI
<p>JUNE 30, 2011
<p>Petr Beckman, A History of Pi(New York: St. Martin’s Press, 1971) Mathematical history centered on the constant pi. Starting in ancient times, but emphasis on work of Archimedes, and on into modern times. Not much on computer techniques.

<hr>
<p>NUMBER THEORY AND ITS HISTORY
<p>FEBRUARY 27, 2011
<p>Oystein Ore, Number Theory and its History (1948; reprint, New York: Dover, 1988)

<p>Good book for an introduction to number theory if you like math. Contains proofs, but avoids mathematical notation so its easier for amateurs. It took me about 2 1/2 months to finish the 360 page book.

<hr>
<p>THE MAN WHO LOVED ONLY NUMBERS
<p>FEBRUARY 27, 2011
<p>Paul Hoffman, The Man Who Loved Only Numbers, The Story of Paul Erdos and the Search for Mathematical Truth (New York: Hyperion, 1998)

<p>This is a quick read about a famous and eccentric 20th century mathematician. He published many papers and there is a system called Erdos numbers where anyone who published a paper with him is given number one, anyone who publishes with that person a number 2, etc.



