<p>I have also written several shorter unpublished articles and lists of transcriptions I made about my White and related families genealogy. In addition there is a map of land grants in Lower Norfolk County in the 1600s I made with the <a href="http://www.directlinesoftware.com/">DeedMapper</a> program. Current research indicates that Thomas White may have been born in Essex. Possibly his family was originally from the manors of Willingale Doe and Shelley. I had some <a title="Essex Manor Records" href="http://jimserver.net/essex-manor-records/">records</a> in Latin that might be relevant translated.

<p>My great-grandfather, <a title="Notes on James McDaniel White" href="http://jimserver.net/notes-on-james-mcdaniel-white/">James McDaniel White</a>, was in the Civil War and was wounded in the the <a href="http://www.nps.gov/pete/historyculture/five-forks.htm">Battle of Five Forks</a>. Another ancestor, John L. Ward, was a soldier in the Revolutionary War. His <a title="John Ward Pension" href="http://jimserver.net/john-ward-pension/">pension application</a> gives an interesting historical account of his service in the war. The earliest records for the White family come from an <a title="White family of Chatham County records" href="http://jimserver.net/white-family-of-chatham-county-records/">old book</a> that was found by my great-uncle. He wrote in the front cover of the book about the book. "This book was found by John E. White in 1925 in a closet in the old White homestead on Rocky River. It was originally the property of my great-grandfather who was born in 1770. It establishes two generations of the White family in Chatham Co., N.C. with accurate birth dates."

<p>Two years ago I joined the <a href="http://huguenot-manakin.org/"> Huguenot Society </a> for the Founders of Manakin in the Colony of Virginia. My qualifying ancestor was named <a href="http://jimserver.net/downloads/JBWhite47922008.pdf"> Abraham Michaux </a> and he was born in Sedan, France, in 1672. <a href="http://en.wikipedia.org/wiki/Huguenots">Huguenots </a> were French Protestants who emigrated from Catholic France. Abraham Michaux settled in Virginia near the Huguenot settlement west of present day Richmond in 1705, 5 years after its establishment.

<p>I submitted a supplemental application for my ancestor <a href="http://jimserver.net/downloads/WhiteJamesNatNo4791-S1.pdf"> John Lanier </a> who was in Virginia before 1660. He was the grandson of Nicholas Lanier and and Lucreece Bassano. Members of the Lanier and Bassano families were court musicians. <a href="http://en.wikipedia.org/wiki/Nicholas_Lanier_the_Elder"> Nicholas Lanier </a> was from Rouen, France, and the <a href="http://en.wikipedia.org/wiki/Anthony_Bassano">Bassano family </a> was from Italy. A cousin, <a href="http://www.shakespeareanauthorshiptrust.org.uk/pages/candidates/other.htm">Amelia Bassano Lanyer</a>, 1569-1645, was an early female author and is included on the list of possible authors of the plays of Shakespeare. I created a <a href="http://jimserver.net/downloads/lanier_descend_chart.svg">tree</a> of the Laniers using the genealogy program <a href="https://gramps-project.org/wiki/index.php?title=Main_page">Gramps</a>.

